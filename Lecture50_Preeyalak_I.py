def plus(x,y):
    print(x+y)
def minus(x,y):
    print(x-y)
def multiply(x,y):
    print(x*y)
def divide(x,y):
    print(x/y)
print("เครื่งคำนวน (บวก, ลบ, คูณ, หาร)")
x = int(input("x = "))
y = int(input("y = "))
print("ผลการบวก =")
plus(x,y)
print("ผลการลบ =")
minus(x,y)
print("ผลการคูณ =")
multiply(x,y)
print("ผลการหาร =")
divide(x,y)


